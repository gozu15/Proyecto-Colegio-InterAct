package Models;

/**
 *
 * @author Elvis Quiroz
 */
public class Horarios {
    private int id;
    private String Hora;
    
    public Horarios(int id, String Hora) {
        this.id = id;
        this.Hora = Hora;
    }
    
    
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getHora() {
        return Hora;
    }

    public void setHora(String Hora) {
        this.Hora = Hora;
    }

    
}
